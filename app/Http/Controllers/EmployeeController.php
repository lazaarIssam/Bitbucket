    {
        $list = DB::table('employees')
                        ->Join('companies', 'employees.companie_id', '=', 'companies.id')
                        ->select('employees.*','companies.Name as compName')
                        ->distinct()
                        ->orderBy('employees.created_at', 'desc')
                        ->paginate(10);
        return view('employees.index',['list' => $list]);
    }

